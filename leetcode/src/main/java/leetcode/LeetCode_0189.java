class Solution_0189 {
    /**
     * @return void
     * @author zhoujie
     * @date 2021/7/6 下午3:58
     * @param: nums
     * @param: k
     * @description: 翻转，先整体翻转，再以k为分割左右分别翻转
     */
    public void rotate(int[] nums, int k) {
        int len = nums.length;
        k %= len;
        reverse(nums, 0, len - 1);
        reverse(nums, 0, k - 1);
        reverse(nums, k, len - 1);

    }

    private void reverse(int[] nums, int i, int j) {
        while (i < j) {
            int temp = nums[i];
            nums[i] = nums[j];
            nums[j] = temp;
            i++;
            j--;
        }
    }
}